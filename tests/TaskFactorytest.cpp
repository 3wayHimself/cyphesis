class TestTaskScriptFactory : public TaskScriptKit {
  protected:
    std::string m_package;
  public:
    virtual const std::string & package() const {
        return m_package;
    }

    virtual int addScript(Task * task) const {
        return 0;
    }

    virtual int refreshClass() {
        return 0;
    }
};

int main()
{
    Character * chr = new Character("1", 1);
    TaskKit * ek;

    Task * e;

    ek = new TaskFactory("test1");

    e = ek->newTask(*chr);
    assert(e);

    delete ek;

    ek = new TaskFactory("test2");

    e = ek->newTask(*chr);
    assert(e);

    ek->m_scriptFactory = new TestTaskScriptFactory;

    delete ek;

    return 0;
}

